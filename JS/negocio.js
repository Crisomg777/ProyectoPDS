function obtenerParametro(parametro) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(parametro);
}

console.log(obtenerParametro('nombre'));

function cargarNegocio() {
    const nombreNegocio = obtenerParametro('nombre');
    console.log(nombreNegocio);
    const negocio = negocios.find(n => n.nombre === nombreNegocio);
    console.log(negocio);

    if (!negocio) {
        document.getElementById('negocio-container').innerHTML = `<p>Negocio no encontrado.</p>`;
        return;
    }

    document.getElementById('negocioNombre').textContent = negocio.nombre;
    const productosLista = document.getElementById('productosLista');
    productosLista.innerHTML = negocio.productos.map(producto => `
        <div class="producto">
            <img src="${producto.imagen}" alt="${producto.nombre}">
            <h3>${producto.nombre}</h3>
            <p>Precio: $${producto.precio.toFixed(2)}</p>
        </div>
    `).join('');
}

window.onload = cargarNegocio;   // Cambiado para asignar la funci√≥n sin ejecutarla inmediatamente
